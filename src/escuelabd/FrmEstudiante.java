    private void cargarCombo(JComboBox c) {
        DefaultComboBoxModel combo=new DefaultComboBoxModel();
  c.setModel(combo);
  listadoperiodos lp=new listadoperiodos();
  try{
    Statement st=conn.createStatement();
    ResultSet rs=st.executeQuery("SELECT NombrePeriodo FROM periodo;");
    
    while (rs.next()) {
       Periodo per=new Periodo();
       per.setNombrePeriodo(rs.getString(1));
       lp.AgregarPeriodos(per);
       combo.addElement(per.getNombrePeriodo());
       System.out.println("Exito.....");
    }
            
  } catch(Exception e) {
      System.out.println("Error,No se puede mostrar combo"+e);
      
  }
  
    }
    private void cargarComboCarreras(JComboBox c) {
  DefaultComboBoxModel combo=new DefaultComboBoxModel();
  c.setModel(combo);
  listadocarreras lc=new listadocarreras();
  try{
    Statement st=conn.createStatement();
    ResultSet rs=st.executeQuery("SELECT Nombre FROM carrera;");
    
    while (rs.next()) {
       Carrera car=new Carrera();
       car.setNombre(rs.getString(1));
       lc.AgregarCarrera(car);
       combo.addElement(car.getNombre());
       System.out.println("Exito.....");
    }
            
  } catch(Exception e) {
      System.out.println("Error,No se puede mostrar combo"+e);
      
  }
  
    }   
